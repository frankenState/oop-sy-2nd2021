    private void student_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_student_dataMouseClicked
       
        int row = this.student_data.getSelectedRow();
        
        Object id = this.student_data.getValueAt(row, 0);
        Object firstname = this.student_data.getValueAt(row, 1);
        Object lastname = this.student_data.getValueAt(row, 2);
        Object midterm = this.student_data.getValueAt(row, 3);
        Object fainlterm = this.student_data.getValueAt(row, 4);
        
        this.idTxt.setText(String.valueOf(id));
        this.firstnameTxt.setText(String.valueOf(firstname));
        this.lastnameTxt.setText(String.valueOf(lastname));
        this.midtermTxt.setText(String.valueOf(midterm));
        this.finalTxt.setText(String.valueOf(fainlterm));
        
        
        this.isUpdate = true;
        this.deleteBtn.setEnabled(true);
    }//GEN-LAST:event_student_dataMouseClicked

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        if (this.isUpdate){
           boolean succss =  this.db.executeSQL(
               "DELETE FROM students WHERE students.id=?",
               new String[]{this.idTxt.getText()}
            );
           
           if (!succss){
                JOptionPane.showMessageDialog(this, "Error in deleting the selected data.", "ERROR MESSAGE", JOptionPane.ERROR_MESSAGE);
           }
           
           this.view();
           this.deleteBtn.setEnabled(false);
           this.clearFields();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

