    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
      
       String firstname = this.firstnameTxt.getText();
       String lastname = this.lastnameTxt.getText();
       int midterm = Integer.parseInt(this.midtermTxt.getText());
       int final_grade = Integer.parseInt(this.finalTxt.getText());
        
       boolean success;
       if (this.isUpdate){
           int id = Integer.parseInt(this.idTxt.getText());
            success = db.executeSQL(
              "UPDATE students SET first_name=?, last_name=?, midterm=?, final=? WHERE students.id=?",
              new Object[]{firstname, lastname, midterm, final_grade, id}
            );
       } else {
            success = db.executeSQL(
              "INSERT INTO students( first_name,last_name, midterm, final) VALUES(?,?,?,?)",
              new Object[]{firstname, lastname, midterm, final_grade}
            );
       }
        
       
       if (!success){
           JOptionPane.showMessageDialog(this, "Error in adding new data.", "ERROR MESSAGE", JOptionPane.ERROR_MESSAGE);
       } else {
          this.view();
          this.clearFields();
          this.isUpdate = false;
          this.deleteBtn.setEnabled(false);
       }
           
       
       
    }//GEN-LAST:event_saveBtnActionPerformed

