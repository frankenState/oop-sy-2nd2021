    public void view(){
        
        DefaultTableModel model = (DefaultTableModel)this.student_data.getModel();
        
        model.setRowCount(0);
        
        ArrayList<Object[]> data = db.getData(
                "SELECT * FROM students", 
                new String[]{"id","last_name","first_name", "midterm","final"}
        );
        
        for (Object[] row : data){
            model.addRow(row);
        }
        
        this.student_data.setModel(model);
                
    }
    
